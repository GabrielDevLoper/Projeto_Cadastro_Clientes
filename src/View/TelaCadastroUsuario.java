    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Usuario u = new Usuario();
        UsuarioDAO udao = new UsuarioDAO();
        if (txtSenha.getText().toUpperCase().equalsIgnoreCase(txtConfirm.getText().toUpperCase()) ) {

            //Setando valores da caixa de texto no objeto cliente.
            u.setLogin(txtLogin.getText().toUpperCase());
            u.setSenha(txtSenha.getText().toUpperCase());

            udao.Salvar(u);
        } else {
            JOptionPane.showMessageDialog(null, "As senhas n√£o se equivalem");
            txtConfirm.setText("");
            txtConfirm.requestFocus();

        }
        txtLogin.setText("");
        txtSenha.setText("");
        txtConfirm.setText("");


    }//GEN-LAST:event_btnSalvarActionPerformed


